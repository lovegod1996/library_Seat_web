public class WeekData {
    private String venue;
    private Integer week;
    private Integer learntime;
    private Integer allLearn;
    private Integer undeal;
    private Integer dealpro;

    public String getVenue() {
        return venue;
    }

    public void setVenue(String venue) {
        this.venue = venue;
    }

    public Integer getLearntime() {
        return learntime;
    }

    public void setLearntime(Integer learntime) {
        this.learntime = learntime;
    }

    public Integer getAllLearn() {
        return allLearn;
    }

    public void setAllLearn(Integer allLearn) {
        this.allLearn = allLearn;
    }

    public Integer getUndeal() {
        return undeal;
    }

    public void setUndeal(Integer undeal) {
        this.undeal = undeal;
    }

    public Integer getDealpro() {
        return dealpro;
    }

    public void setDealpro(Integer dealpro) {
        this.dealpro = dealpro;
    }

    public Integer getWeek() {
        return week;
    }

    public void setWeek(Integer week) {
        this.week = week;
    }
}
